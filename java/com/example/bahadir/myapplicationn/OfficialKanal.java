package com.example.bahadir.myapplicationn;

public class OfficialKanal {

    String kanaladi;
    String kanalurl;
    String id;
    String date;
    int like;

    public OfficialKanal(){

    }

    public String getKanaladi() {
        return kanaladi;
    }
    public void setKanaladi(String kanaladi) {
        this.kanaladi = kanaladi;
    }
    public String getKanalurl() {
        return kanalurl;
    }
    public void setKanalurl(String kanalurl) {
        this.kanalurl = kanalurl;
    }
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public int getLike() {
        return like;
    }
    public void setLike(int like) {
        this.like = like;
    }
    public String getDate() {
        return date;
    }
    public void setDate(String date) {
        this.date = date;
    }
}
